#program with two function
def multiply(x,y):
    '''
    This function takes two numbers as input and multiplys them
    '''
    print("multiply result = ",(x*y))

def sub(a,b):
    '''
    This function takes two numbers as input and subtracts them
    '''
    print("sub result = ",(a-b))
    
#function calling
    
multiply(4,6)
sub(7,1)
